
# Exercicios matemáticos


n1 = float(input('Digite um número qualquer: '))
n2 = float(input('Digite outro número qualquer: '))
soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
potencia = n1 ** n2
divInt = n1 // n2
resto = n1 % n2
print(f'SOMA: {n1} + {n2} = {soma}')
print(f'SUBTRAÇÃO: {n1} - {n2} = {subtracao}')
print(f'MULTIPLICAÇÃO: {n1} * {n2} = {multiplicacao}')
print(f'DIVISÃO: {n1} / {n2} = {divisao}')
print(f'POTENCIAÇÃO: {n1} ** {n2} = {potencia}')
print(f'DIVISÃO INTEIRA: {n1} // {n2} = {divInt}')
print(f'RESTO (OU MÓDULO) DA DIVISÃO: {n1} % {n2} = {resto}')


